#include "main.h"
/**
 * mul - This function do multiplication of two integers.
 * @a: This is first number.
 * @b: This is second number.
 *
 * Description: This is a function that multiplies two integers.
 *
 * Return: Always 0 (Success)
 */
int mul(int a, int b)
{
	int res;

	res = a * b;
	return (res);
}
